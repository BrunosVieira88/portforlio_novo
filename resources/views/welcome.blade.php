    var isMatrixActive = false;
    const audio = new Audio('matrix.mp3');

    function Begin() {

        let textElement = document.getElementById('matrix-text');
        let iconElements = document.querySelectorAll('.icon_color');



        if (isMatrixActive) {
            audio.pause();

            textElement.setAttribute('hidden', true);
            document.body.style.backgroundColor = "";
            isMatrixActive = false;
            for (let icon of iconElements) {
                icon.style.color = 'blue';
            }

        } else {
            audio.play();
            textElement.removeAttribute('hidden');
            let changeColor = document.getElementById("changeColor");
            darkenBackgroundColor(changeColor, 2000);
            isMatrixActive = true;
            for (let icon of iconElements) {
                icon.style.color = 'green';
            }
            createMatrixEffect();
        }

        function darkenBackgroundColor(element, duration) {
            let currentColor = window.getComputedStyle(element).backgroundColor;
            let currentRed = parseInt(currentColor.substr(4, 3));
            let currentGreen = parseInt(currentColor.substr(9, 3));
            let currentBlue = parseInt(currentColor.substr(14, 3));
            let targetRed = 0;
            let targetGreen = 0;
            let targetBlue = 0;
            let steps = 50; // Number of steps for the animation
            let stepInterval = duration / steps;

            let stepRed = (currentRed - targetRed) / steps;
            let stepGreen = (currentGreen - targetGreen) / steps;
            let stepBlue = (currentBlue - targetBlue) / steps;

            let currentStep = 0;

            let interval = setInterval(function () {
                currentRed -= stepRed;
                currentGreen -= stepGreen;
                currentBlue -= stepBlue;
                element.style.backgroundColor = `rgb(${currentRed}, ${currentGreen}, ${currentBlue})`;

                currentStep++;
                if (currentStep >= steps) {
                    clearInterval(interval);
                }
            }, stepInterval);
        }


    }
